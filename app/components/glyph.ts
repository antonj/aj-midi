export function drawGlyph(
  ctx: CanvasRenderingContext2D,
  id: keyof typeof paths,
  x: number,
  y: number,
  noteHeight: number,
  fillStyle: string = "black",
  invertY = false,
) {
  ctx.save();
  const w = 250;
  const p = paths[id];
  const invertYaxis = invertY ? 1 : -1;
  ctx.translate(x - noteHeight * p.dx, y + noteHeight * p.dy);
  ctx.scale(noteHeight / w, (invertYaxis * noteHeight) / w);

  ctx.fillStyle = fillStyle;

  const path = new Path2D(p.p);
  ctx.fill(path);

  ctx.restore();
}
